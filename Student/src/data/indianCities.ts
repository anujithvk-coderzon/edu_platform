/**
 * Search cities by query string
 * Case-insensitive search that matches from the start of the city name
 */
export function searchCities(query: string, limit: number = 10): string[] {
  if (!query || query.trim() === '') {
    return [];
  }

  const normalizedQuery = query.toLowerCase().trim();

  // First, find cities that start with the query
  const startsWithMatches = indianCities.filter(city =>
    city.toLowerCase().startsWith(normalizedQuery)
  );

  // Then, find cities that contain the query (but don't start with it)
  const containsMatches = indianCities.filter(city => {
    const lowerCity = city.toLowerCase();
    return lowerCity.includes(normalizedQuery) && !lowerCity.startsWith(normalizedQuery);
  });

  // Combine results: prioritize starts-with matches
  const allMatches = [...startsWithMatches, ...containsMatches];

  return allMatches.slice(0, limit);
}
